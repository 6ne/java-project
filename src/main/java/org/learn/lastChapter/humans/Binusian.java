package org.learn.lastChapter.humans;

public abstract class Binusian {
  private String name;
  private Integer age;

  protected Binusian(String name, Integer age) {
    this.name = name;
    this.age = age;
  }

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public Integer getAge() {
    return age;
  }

  public void setAge(Integer age) {
    this.age = age;
  }
}
